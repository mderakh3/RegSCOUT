#!/usr/bin/env Rscript

# Load the Seurat package
library(Seurat)

# Load the Seurat object
seurat_object <- readRDS('/home/mderakh3/moho/scIBD.gex_matrix.rds')

# Print a general summary of the Seurat object
cat("Summary of Seurat object:\n")
print(summary(seurat_object))

# Print the structure of the Seurat object
cat("\nStructure of the Seurat object:\n")
str(seurat_object)

# Print the names of available slots in the Seurat object
cat("\nAvailable slots in the Seurat object:\n")
print(names(seurat_object))

# Print the first few entries of each slot if available
cat("\nFirst few entries of each slot (if applicable):\n")
for (slot_name in names(seurat_object)) {
  cat("\nSlot:", slot_name, "\n")
  slot_content <- slot(seurat_object, slot_name)
  if (is.list(slot_content) || is.data.frame(slot_content) || is.matrix(slot_content)) {
    print(head(slot_content))
  } else {
    cat("Content type:", class(slot_content), "\n")
  }
}

